          <summary>La canette une "réserve d'énergie"</summary>
          <p class="blabla_readmore">Toutes les canettes sont recyclables à 100 % : une fois collectées et parvenues aux sites de recyclage, pas le moindre gramme d'aluminium ne se perd.
          On peut la faire fondre et la réutiliser à l'infini sans aucune perte de qualité. Le recyclage de l'aluminium récupéré permet d'économiser 95 % d'énergie par rapport à l'utilisation d'aluminium issu des réserves naturelles. Aujourd’hui, le poids d'une canette de Power Freak a diminué significativement comparé aux années précédentes. La diminution constante du poids fait aussi la différence : moins on utilise d'aluminium dans les canettes en début de cycle, plus petite sera l'empreinte carbone.
          </p>
        </details>
        <details>
          <summary>Chaque canette compte</summary>
          <p class="blabla_readmore">Les canettes sont les emballages de boisson les plus recyclés au monde. Bien évidemment, ceci n'est possible que si les canettes sont récupérées et (ré)introduites dans la boucle de recyclage. Naturellement, si on laisse négligemment une canette dans la nature, elle ne peut pas réaliser son potentiel de recyclage. Malheureusement, dans certains pays, les détritus sont encore jetés n’importe où. Jeter la canette dans une poubelle ou un bac de tri, contribue à préserver la beauté de nos paysages, à améliorer les taux de recyclage, à réduire la consommation d'énergie ainsi que l'épuisement des ressources naturelles. Si votre pays dispose d'une filière de recyclage de l'aluminium, jetez votre canette de manière appropriée.
          </p>
